public class ShortExample {
    public static void main(String[] args) {
        short  x = 1000;
        short y = 2000;

        short sum = (short) (x+y);

        System.out.println("First short value: " + x);
        System.out.println("Second short value: " + y);
        System.out.println("Sum of shorts: " + sum);
    }
}