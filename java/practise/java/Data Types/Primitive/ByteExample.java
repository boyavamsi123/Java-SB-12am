public class ByteExample {
    public static void main(String[] args) {
        byte a = 10;
        byte b = 20;

        byte sum = (byte) (a+b);

        System.out.println("First byte value: " + a);
        System.out.println("Second byte value: " + b);
        System.out.println("Sum of bytes: " + sum);
    }
}