public class IntExample {
    public static void main(String[] args) {
        int num1 = 5000;
        int num2 = 7000;

        int sum = num1 + num2;

        System.out.println("First int value: " + num1);
        System.out.println("Second int value: " + num2);
        System.out.println("Sum of ints: " + sum);
    }
}