public class FloatExample {
    public static void main(String[] args) {
        float price = 10.75f;
        float tax = 1.25f;

        float total = price + tax;

        System.out.println("Item price: $" + price);
        System.out.println("Tax: $" + tax);
        System.out.println("Total price: $" + total);
    }
}